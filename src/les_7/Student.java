package les_7;

class Student {
    String lastName;
    String firstName;
    int grade;
    String subject;
    int score;

    Student(String lastName, String firstName, int grade, String subject, int score) {
        this.lastName = lastName;
        this.firstName = firstName;
        this.grade = grade;
        this.subject = subject;
        this.score = score;
    }
}